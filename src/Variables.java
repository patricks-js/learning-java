public class Variables {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 5;

        int sum = num1 + num2;
        int sub = num1 - num2;
        int div = num1 / num2;
        int multi = num1 * num2;

        System.out.println(sum);
        System.out.println(sub);
        System.out.println(div);
        System.out.println(multi);
    }
}
